n = int(input())
# Сразу отсекаем числа вне диапазона (else: «ошибка ввода»)
if 0 <= n < 37:
# Раскидываем все комбинации
    if ((0 < n < 11) or (18 < n < 29)) and (n % 2) == 0:
        print('черный')
    elif ((10 < n < 19) or (28 < n < 37)) and (n % 2) != 0:
        print('черный')
    elif n == 0:
        print('зеленый')
    else:
        print('красный')
else:
    print('ошибка ввода')
